function populateTable(page, data, table) {
    const tableBody = table;
    tableBody.innerHTML = ''; // Clear previous table data
    const totalPages = Math.round(data.length / 10)
    const startIndex = (page - 1) * 10;
    const endIndex = startIndex + 10;
    const currentPageData = data.slice(startIndex, endIndex);

    let tableData = currentPageData.map((row) => {
        return `
        <div class="table-row">
            <h5 class="td-name">${row.name}</h5>
            <p class="td-time">${row.dataTime}</p>
            <p class="td-status">${row.paymentStatus}</p>
            <h5 class="td-money">${row.amount}</h5>
        </div>`
    }).join("");
    tableBody.innerHTML = tableData
    // create navigators, pageIndicator and adding them into tbody
    const navigatorContainer = document.createElement("div")
    navigatorContainer.classList.add("tableNavigators");
    navigatorContainer.innerHTML = `<i class="fa-solid fa-circle-arrow-left " id="previousButton"></i>
    <p class="pageIndicator" id="pageIndicator">1</p>
    <i class="fa-solid fa-circle-arrow-right" id="nextButton"></i>`
    tableBody.appendChild(navigatorContainer)

    // adding event listeners on buttons for navigate table
    document.getElementById('previousButton').addEventListener('click', () => {
        goToPage(currentPage - 1, data, table);
    });

    document.getElementById('nextButton').addEventListener('click', () => {
        goToPage(currentPage + 1, data, table);
    });

    updatePageIndicator(totalPages)
}
let currentPage = 1; // Initial page number

function goToPage(page, data, table) {
    if (page < 1 || page > Math.ceil(data.length / 10)) {
        return; // Invalid page number
    }
    currentPage = page
        ;
    populateTable(currentPage, data, table);
    updatePageIndicator();
}

function updatePageIndicator() {
    console.log()
    const pageIndicator = document.getElementById('pageIndicator');
    pageIndicator.textContent = `${currentPage}`;
}


